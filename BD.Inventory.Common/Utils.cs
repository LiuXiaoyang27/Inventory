        /// <summary>
        /// 将月份转成天数
        /// </summary>
        /// <param name="startDate"></param>
        /// <param name="fractionalMonths"></param>
        /// <returns></returns>
        public static double ConvertMonthToDays(DateTime startDate, double fractionalMonths)
        {
            int wholeMonths = (int)fractionalMonths; // 整数部分表示完整的月份数
            double remainingDaysFraction = fractionalMonths - wholeMonths; // 小数部分表示剩余的天数比例

            // 计算整数部分月份的天数总和
            double totalWholeMonthDays = 0;
            for (int i = 0; i < wholeMonths; i++)
            {
                DateTime nextMonth = startDate.AddMonths(i + 1); // 下一个月的日期
                totalWholeMonthDays += (nextMonth - startDate).Days; // 累加两个日期之间的天数
                startDate = nextMonth; // 更新起始日期为下一个月的第一天
            }

            // 计算小数部分对应的天数
            double remainingDays = remainingDaysFraction * GetDaysInMonth(startDate); // 小数部分乘以当前月的天数

            // 返回总天数（整数部分的天数 + 小数部分的天数）
            return totalWholeMonthDays + remainingDays;
        }

        /// <summary>
        /// 使用DateTime的DaysInMonth方法获取指定年月的天数
        /// </summary>
        /// <param name="date"></param>
        /// <returns></returns>
        private static int GetDaysInMonth(DateTime date)
        {
            return DateTime.DaysInMonth(date.Year, date.Month);
        }

